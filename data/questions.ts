// Функція для отримання випадкового запитання
export const getRandomQuestion = (): string => {
  const randomIndex = Math.floor(Math.random() * SOUL_QUESTIONS.length);
  return SOUL_QUESTIONS[randomIndex];
};

// Функція для отримання запитання дня (одне і те саме для всіх користувачів на день)
export const getQuestionOfTheDay = (): string => {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000
  );
  const index = dayOfYear % SOUL_QUESTIONS.length;
  return SOUL_QUESTIONS[index];
};
